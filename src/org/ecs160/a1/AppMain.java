    private void showXYST(Vector<Double> xyst) {
        DecimalFormat df;

        if (decPlaces == 0) {
            df = new DecimalFormat("#0");
        } else if (decPlaces == 2) {
            df = new DecimalFormat("#0.00");
        } else {
            df = new DecimalFormat("#0.000");
        }

        TextComponent x = new TextComponent().label("X:" + df.format(xyst.get(0)));
        TextComponent y = new TextComponent().label("Y:" + df.format(xyst.get(1)));
        TextComponent s = new TextComponent().label("S:" + df.format(xyst.get(2)));
        TextComponent t = new TextComponent().label("T:" + df.format(xyst.get(3)));
        xRegister.removeAll();
        yRegister.removeAll();
        sRegister.removeAll();
        tRegister.removeAll();
        xRegister.add(x);
        yRegister.add(y);
        sRegister.add(s);
        tRegister.add(t);

        TextComponent q = new TextComponent().label("Stack Size:" + normalal.getCursize());
        textbar.removeAll();
        textbar.add(q);
        show();
    }

    private void fix() {
        if (decPlaces == 0) {
            decPlaces = 2;
        } else if (decPlaces == 2) {
            decPlaces = 3;
        } else {
            decPlaces = 0;
        }
    }

